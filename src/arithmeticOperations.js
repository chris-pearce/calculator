/**
 * @function add
 * @description Add two numbers.
 * @param  {string} x - Addend.
 * @param  {string} y - Addend.
 * @return {number} - The sum of both addends.
 */
export const add = (x, y) => Number(x) + Number(y);


/**
 * @function subtract
 * @description Subtract one number away from another.
 * @param  {string} x - Minuend.
 * @param  {string} y - Subtrahend.
 * @return {number} - The result of subtracting minuend from subtrahend.
 */
export const subtract = (x, y) => Number(x) - Number(y);


/**
 * @function multiply
 * @description Multiply two numbers.
 * @param  {string} x - Number one.
 * @param  {string} y - Number two.
 * @return {number} - The result of multiplying number one by number two.
 */
export const multiply = (x, y) => Number(x) * Number(y);


/**
 * @function divide
 * @description Divide two numbers.
 * @param  {string} x - Dividend.
 * @param  {string} y - Divisor.
 * @return {number} - The quotient.
 */
export const divide = (x, y) => Number(x) / Number(y);
